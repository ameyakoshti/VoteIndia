								<p>
									Chavan started his political career in 1991 in place of his mother, and won the family seat of Karad thrice, in '91, '96 and '98 and lost it in 1999. [1]
									He was the Minister of State for the ministries of Ministry of Science and Technology, Ministry of Earth Sciences, Ministry of Personnel, Public Grievances and Pensions, Ministry of Parliamentary Affairs and in charge of Prime Minister's Office. Earlier he was also the Minister Of State in charge of the Department Of Atomic Energy. He retained his image of "Mr. Clean" which fetched prestigious post of chief minister after departure of Ashok Chavan on account of Adarsh Scam. Barely days into his tenure as the Chief Minister Of Maharashtra, he, along with Civil Aviation minister Praful Patel, met Jairam Ramesh, the Minister for Environment And Forests and successfully convinced him to give the environmental clearance for the much delayed Navi Mumbai International Airport. He was instrumental in fostering establishment of Jaitapur Power Project through direct counseling of natizens. He has also announced reformations in higher and technical education in Maharashtra. The research at the University level has been promoted by providing enhanced Scholarships so that people can take up the work for full time. In the very first year, staunch steps to curb corruption in cooperative sector was taken by him through RBI by dissolution of Directorate of Maharashtra State Cooperative Bank. The administrators appointed by state government brought back the bank into profit through huge debt recovery. He had also put down builder lobby having background of unfair practices in urban areas especially in Mumbai and Pune. Despite this, he has been widely criticized by his party fellows, allies and opposition for not taking decisions on developmental issues by keeping files unsigned ranging from Infra Projects, Toll Issues, Sports Awards to Agriculture & Industry policy related issues.He took key decision in reforming Tax structures at local body levels through replacement of Octroi by Local Body Tax (LBT). Although his move was upheld by courts,he faced widespread agitation from merchants especially Gujrathi & Jain. He initiated a Government Resolution on the long pending issue of development of cessed buildings thereby doing away with he conventional Mantralaya brokers who reigned the fate of lakhs of middle class populace of the city of Mumbai for over decades. Mr. Chavan is known to have done away with political touts in the corridors of power as he executes crucial matters via policy decisions so that to serve masses rather than classes. There has been agony about his clean & transparent working style among cabinet colleagues. There are two former chief ministers serving in his cabinet; and both of them are infamous for corruption. Plus there are a couple of others who are aspiring contenders for the Office of Chief Minister. Mr. Chavan however, enjoys total confidence of the party high command as also goodwill of common households in the State.
